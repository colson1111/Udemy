# Using arrays and scalars

import numpy as np
from __future__ import division

# Create array
arr1 = np.array([[1,2,3],[8,9,10]])

# show
arr1

# Multiplying arrays
arr1 * arr1

# Subtraction
arr1 - arr1

# Arithmetic operations with scalars on array
1 / arr1

# Exponential operation
arr1 ** 3

# modulo
arr1 % 3
